package com.basic.projectbook.vo;

public class StoreMapVO {
private int no;
private int storeid;
private String storename;
private double location1;
private double location2;
private String img;
private String address;

public int getNo() {
	return no;
}
public void setNo(int no) {
	this.no = no;
}
public int getStoreid() {
	return storeid;
}
public void setStoreid(int storeid) {
	this.storeid = storeid;
}
public String getStorename() {
	return storename;
}
public void setStorename(String storename) {
	this.storename = storename;
}
public double getLocation1() {
	return location1;
}
public void setLocation1(double location1) {
	this.location1 = location1;
}
public double getLocation2() {
	return location2;
}
public void setLocation2(double location2) {
	this.location2 = location2;
}
public String getImg() {
	return img;
}
public void setImg(String img) {
	this.img = img;
}
public String getAddress() {
	return address;
}
public void setAddress(String address) {
	this.address = address;
}

}
